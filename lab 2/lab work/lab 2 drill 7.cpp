// Program to print "Usama Siraj" multiple times using a for loop

#include <iostream>      // iostream library input/output ke liye use hoti hai
using namespace std;     // std namespace use kar rahe hain taake cout directly use ho sake

int main() 
{
	for (int i = 0; i < 2; i++)   // Loop start 0 se, 2 se chhota tak chalega (0 aur 1 iterations)
	{
		cout << "Usama Siraj\n";  // Har iteration me name print karega
	}

	return 0;                     // Program successfully end ho jata hai
}
